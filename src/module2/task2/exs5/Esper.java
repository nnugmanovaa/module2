package module2.task2.exs5;

public class Esper {
    private String title;
    private int mighty;
    private String type;

    public Esper(String title, int mighty, String type) {
        this.title = title;
        this.mighty = mighty;
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getMighty() {
        return mighty;
    }

    public void setMighty(int mighty) {
        this.mighty = mighty;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
